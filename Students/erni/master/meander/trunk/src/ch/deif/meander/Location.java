package ch.deif.meander;


public class Location {

    public double xNormed, yNormed;
    public double height;
    
    public Location(double xNormed, double yNormed, double height) {
        this.xNormed = xNormed;
        this.yNormed = yNormed;
        this.height = height;
    }
    
}
